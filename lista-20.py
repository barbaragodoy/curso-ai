# LISTA 20 - Exercício de Cálculo de Desconto
# Objetivo: Calcular o preço final de um produto com desconto
# ========================================

# Define o preço do produto e o percentual de desconto
produto = 30.00
percentual_desconto = 0.15 

# Calcula o valor do desconto
desconto_final = produto * percentual_desconto

# Calcula o preço final (produto + desconto)
preco_final = produto + desconto_final

# Exibe o preço final formatado
print(f"O preço final do produto: R$ {preco_final:.2f}")