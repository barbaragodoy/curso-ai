# ========================================
# LISTA 10 - Exercício de Divisão com Resto
# Objetivo: Receber dois valores e calcular quociente e resto
# ========================================

# Recebe dois valores decimais do usuário
numero1 = float(input("insira o valor 1: "))
numero2 = float(input("insira o valor 2: "))

# Calcula o quociente da divisão
quociente = (numero1 / numero2)
# Calcula o resto da divisão
resto = (numero1 % numero2)

# Exibe o quociente e o resto da divisão
print (quociente, resto)