# ========================================
# DADOS PRINT - Exemplo de Entrada e Saída de Dados
# Objetivo: Demonstrar como receber e exibir dados do usuário
# ========================================

# Recebe o nome do usuário (tipo string)
nome = input("Insira seu nome: ")
# Recebe a idade do usuário (tipo string)
idade = input("Insira sua idade: ")

# Exibe uma saudação personalizada concatenando strings
print ("Olá, " + nome + "!")
# Exibe a idade informada concatenando strings
print ("Você tem " + idade + " anos.")