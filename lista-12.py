# LISTA 12 - Exercício de Área do Retângulo
# Objetivo: Calcular a área de um retângulo
# ========================================

# Recebe a base e altura do retângulo
base = float(input("insira o valor da base: "))
altura = float(input("insira o valor da altura: "))

# Calcula a área do retângulo (base × altura)
area = base * altura

# Exibe o resultado da área
print (area)